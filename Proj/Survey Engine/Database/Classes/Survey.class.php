<?php

class Survey extends Dbh {

    protected function getAllSurveys() {
        $sql = "SELECT * FROM survey";
        $result = $this->connect()->query($sql);
        $numRows = $result->num_rows;
        if ($numRows > 0) {
            while ($row = $result->fetch_assoc()) {
                $data[] = $row;
            }
            return $data;
        }
    }

    protected function getASurvey($query_id) {
        $sql = "SELECT * FROM survey WHERE `id` = $query_id;";
        $result = $this->connect()->query($sql);
        $numRows = $result->num_rows;
        if ($numRows > 0) {
            while ($row = $result->fetch_assoc()) {
                $data[] = $row;
            }
            return $data;
        }
    }

}

?>